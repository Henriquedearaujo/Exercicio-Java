public class MediaNotas {
    public static void main(String[] args) {
        double nota1 = 10;
        double nota2 = 7;
        double nota3 = 5;
        double nota4 = 9;

        double notas = (nota1 + nota2 + nota3 + nota4);

        // Calcular a média
        double media = (notas) / 4;

        // Exibir a média
        System.out.println("A média das quatro notas é: " + media);
    }
}
